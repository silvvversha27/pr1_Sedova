# Задание №4:
print("Задание №4:")
# Читаем входные данные
N, K = map(int, input("Введите количество дней в году и количество политических партий (через пробел): ").split())
strike_days = set()  # Множество для хранения дней, когда объявляют забастовку

# Обрабатываем данные по каждой партии
for i in range(K):
    a_i, b_i = map(int, input(f"Введите данные для {i+1}-й партии (начальный день и периодичность через пробел): ").split())
    day = a_i  # Начальный день для забастовок партии
    while day <= N:
        if day % 7 != 6 and day % 7 != 0:  # Проверяем, что это не суббота и не воскресенье
            strike_days.add(day)
        day += b_i  # Переходим к следующему дню забастовки

# Определяем число забастовок за год
total_strikes = len(strike_days)

print(f"Общее количество забастовок в течение года: {total_strikes}")

# Задание №5
print("Задание №5:")
# Запрашиваем количество строк текста
num_lines = int(input("Введите количество строк текста: "))

# Создаем пустой словарь для хранения слов и их частоты
word_count = {}

# Считываем строки текста и анализируем слова
for _ in range(num_lines):
    text_line = input("Напишите строку текста: ")
    words = text_line.split()
    for word in words:
        # Удаляем знаки пунктуации, если они есть (для простоты предполагаем, что их нет)
        word = word.strip('.,!?')
        # Преобразуем слово в нижний регистр, чтобы учесть регистронезависимость
        word = word.lower()
        # Обновляем словарь
        word_count[word] = word_count.get(word, 0) + 1

# Создаем упорядоченный список слов и их частоты
sorted_word_count = sorted(word_count.items())

# Выводим результат
print("Упорядоченный список слов по алфавиту и их частоты:")
for word, count in sorted_word_count:
    print(f"{word}: {count}")
